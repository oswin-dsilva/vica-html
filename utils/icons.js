/**
 * Creates an SVG icon element from the icons library
 * @param {string} name - The name of the icon to create
 * @param {Object} options - Optional attributes for the SVG element
 * @returns {string} SVG markup string
 */
export function createIcon(name, options = {}) {
  const iconSvg = icons[name];
  
  if (!iconSvg) {
    console.warn(`Icon "${name}" not found. Available icons:`, Object.keys(icons));
    // Return a default placeholder icon
    return `<svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
      <circle cx="12" cy="12" r="10"></circle>
      <line x1="12" y1="8" x2="12" y2="12"></line>
      <line x1="12" y1="16" x2="12.01" y2="16"></line>
    </svg>`;
  }
  
  // Apply custom attributes if provided
  let modifiedSvg = iconSvg;
  if (options.width) {
    modifiedSvg = modifiedSvg.replace(/width="[^"]*"/, `width="${options.width}"`);
  }
  if (options.height) {
    modifiedSvg = modifiedSvg.replace(/height="[^"]*"/, `height="${options.height}"`);
  }
  if (options.className) {
    modifiedSvg = modifiedSvg.replace('<svg', `<svg class="${options.className}"`);
  }
  
  return modifiedSvg;
}

/**
 * Gets a list of all available icon names
 * @returns {string[]} Array of icon names
 */
export function getAvailableIcons() {
  return Object.keys(icons);
}

/**
 * Creates an icon element and returns it as a DOM element
 * @param {string} name - The name of the icon to create
 * @param {Object} options - Optional attributes for the SVG element
 * @returns {Element} SVG DOM element
 */
export function createIconElement(name, options = {}) {
  const iconMarkup = createIcon(name, options);
  const template = document.createElement('template');
  template.innerHTML = iconMarkup.trim();
  return template.content.firstChild;
}

// Default export for convenience
export default createIcon;
